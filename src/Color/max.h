inline signed char max(signed char x)						// max (signed char)
{
	return SCHAR_MAX;
} // end function max


inline short max(short x)									// max (short)
{
	return SHRT_MAX;
} // end function max


inline int max(int x)										// max (int)
{
	return INT_MAX;
} // end function max


inline float max(float x)									// max (float)
{
	return 1.0;
} // end function max


inline double max(double x)									// max (double)
{
	return 1.0;
} // end function max


inline unsigned char max(unsigned char x)					// max (unsigned char)
{
	return UCHAR_MAX;
} // end function max


inline unsigned short max(unsigned short x)					// max (unsigned short)
{
	return USHRT_MAX;
} // end function max


inline unsigned int max(unsigned int x)						// max (unsigned int)
{
	return UINT_MAX;
} // end function max