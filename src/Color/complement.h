inline signed char complement(signed char x)						// complement (signed char)
{
	return ~x;
} // end function complement


inline short complement(short x)									// complement (short)
{
	return ~x;
} // end function complement


inline int complement(int x)										// complement (int)
{
	return ~x;
} // end function complement


inline float complement(float x)									// complement (float)
{
	return 1 - x;
} // end function complement


inline double complement(double x)									// complement (double)
{
	return 1 - x;
} // end function complement


inline unsigned char complement(unsigned char x)					// complement (unsigned char)
{
	return ~x;
} // end function complement


inline unsigned short complement(unsigned short x)					// complement (unsigned short)
{
	return ~x;
} // end function complement


inline unsigned int complement(unsigned int x)						// complement (unsigned int)
{
	return ~x;
} // end function complement
